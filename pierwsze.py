a = int(input("Podaj liczbę"))
b = int(input("Podaj liczbę"))

# == - obie wartości równe
# > - wartość większa od drugiej
# >= - większy/równy
# < - mniejszy
# <= - mniejszy bądź równy
# != - różne wartości (różne od)
# ! - zanegowanie wartości (odwrócenie wyniku)

if a == b:
    print("Podana wartość a", a,"jest równa do",b)
elif a > b:
    print("Podana wartość a", a,"jest większa od",b)
else:
    print("Podana wartość b", b,"jest większa od",a)







